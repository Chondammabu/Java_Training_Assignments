package basicsjava.ans;

public class Singlecomment {
	public static void main(String[] args) {
		//Single line comment
		System.out.println("//Hello World");
		/*Multiple line
		 * comment
		 */
        System.out.println("/* Hello World */");
        /** Documentation
         * Comment
         * line
         */
        System.out.println("/** Hello world */");
		
	}
        
}
